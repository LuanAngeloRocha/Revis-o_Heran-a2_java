public class Imóvel{
    String endereco;
    double preco;
    
    // Gerando os gets e sets ( métodos de consulta e de acesso)
    public String getEndereco() {
        return endereco;
    }
    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }
    public double getPreco() {
        return preco;
    }
    public void setPreco(double preco) {
        this.preco = preco;
    }

    
}